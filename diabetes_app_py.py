import streamlit as st
import joblib

model = joblib.load('diabetes_model.pkl')

st.title("ðŸ©º Diabetes Prediction App")
st.write("Enter patient details to check diabetes prediction.")

age = st.number_input("Enter Age", min_value=1, max_value=120, value=30)
mass = st.number_input("Enter Body Mass Index (mass)", min_value=10.0, max_value=100.0, value=30.0)
insu = st.number_input("Enter Insulin Level", min_value=0, max_value=900, value=80)
plas = st.number_input("Enter Plasma Glucose Level", min_value=0, max_value=200, value=100)
pres = st.number_input("Enter Blood Pressure", min_value=0, max_value=140, value=70)

if st.button("Predict"):
    result = model.predict([[age, mass, insu, plas, pres]])
    if result[0] == "tested_positive":
        st.error("ðŸ”´ The model predicts **Diabetes**")
    else:
        st.success("ðŸŸ¢ The model predicts **No Diabetes**")